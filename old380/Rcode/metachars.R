Common metachars
? Character or sub-pattern occurs zero or one time + Character or sub-pattern occurs one or more times
Character or sub-pattern occurs zero or more times . Any character
^ Anchor for the beginning of string $ Anchor for the end of string
\< Beginning of a word
\> End of a word
() Group of literals, i.e., a sub-patter
| Alternation, i.e., one sub-pattern or another
{} Quantifier: {n} means exactly n repeats of the literal or sub-
pattern; {n,m} means n to m repeats; and {n,} means n or more repeats
